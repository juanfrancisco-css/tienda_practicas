

function ColorInputInvalidos(){
    document.getElementById("email").style.borderWidth=2;
    document.getElementById("email").style.borderStyle="solid"
    document.getElementById("email").style.borderColor="red"
    document.getElementById("pass").style.borderWidth=2;
    document.getElementById("pass").style.borderStyle="solid"
    document.getElementById("pass").style.borderColor="red"
}
function ColorInputValidos(){
    document.getElementById("email").style.borderWidth=2;
    document.getElementById("email").style.borderStyle="solid"
    document.getElementById("email").style.borderColor="green"
    document.getElementById("pass").style.borderWidth=2;
    document.getElementById("pass").style.borderStyle="solid"
    document.getElementById("pass").style.borderColor="green"
}
